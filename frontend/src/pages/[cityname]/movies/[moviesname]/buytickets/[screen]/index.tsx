const Screens = () => {
const [activeTabKey, setActiveTabKey] = useState<string>("0");
const [selectedTime, setSelectedTime] = useState<any>(screen.timeslots[0]);
const [selectedSeat, setSelectedSeat] = useState<any[]>([]);

const deselectSeat = (seat: any) => {
    console.log(seat)
    const isSelected = selectedSeat.findIndex((s: any) => {
      return seat.rows === s.rows && seat.cols === s.cols && seat.seat_id === s.seat_id;
    });

    if (isSelected !== -1) {
      setSelectedSeat(selectedSeat.filter((s: any) => seat.seat_id !== s.seat_id));
    } else {
      setSelectedSeat([...selectedSeat, seat]);
    }
  };



  const tabList = screen.timeslots.map((time: any, index: any) => ({
    key: String(index),
    label: (
      <Typography.Title
        level={5}
        style={{
          color: selectedTime.time === time.time ? 'red' : undefined,
        }}
        onClick={() => {
          setSelectedTime(time);
          setActiveTabKey(String(index));
        }}
        key={index}
      >
        {time.time}
      </Typography.Title>
    ),
  }));

  const onTabChange = (key: string) => {
    setActiveTabKey(key);
    setSelectedTime(screen.timeslots[Number(key)]);
  };
    


const renderSeats = () => {
    const slotTimeIndex = screen.timeslots.findIndex((time: any) => time.time === selectedTime.time);
    const seatData = screen.timeslots[slotTimeIndex].seats;
    return seatData.map((seatsType: any, index: number) => (
      <Space direction='vertical' key={index}>
         <Divider style={{margin:5}}/>
         <Tag color='purple' style={{borderRadius:25}}>
            {seatsType.type} - Rs. {seatsType.price}

         </Tag>
        
        <Space direction='vertical'>
          {seatsType.rows.map((row: any, rowIndex: number) => (
              <Space key={rowIndex}  >
              <Tag color="#2ca3ff" style={{margin:10}}>{row.rowname}</Tag>
                {row.cols.map((col: any, colIndex: number) => (
                  <Space key={colIndex}>
                    <Divider type='vertical' />
                    {col.seats.map((seat: any, seatIndex: number) => (
                      <Space  key={seatIndex}>
                        <Tag
                        color={
                          selectedSeat.find((s: any) => (
                            s.rows === row.rowname && s.seat_id === seat.seat_id && s.cols === colIndex
                          )) ? 'blue' : `${seat.status === 'available' ? 'green' : 'red'}`
                        }
                        style={{
                          padding: 0,
                          cursor: seat.status === 'available' ? 'pointer' : 'not-allowed',
                        }}
                        key={seatIndex}
                      >
                        <SeatButton
                          disabled={seat.status !== 'available'}
                          onClick={() => deselectSeat({
                            rows: row.rowname,
                            cols: colIndex,
                            seat_id: seat.seat_id,
                            price: seatsType.price,
                          })}
                        >
                          {seatIndex + 1}
                        </SeatButton>
                      </Tag>
                      </Space>
                    ))}

                  </Space>
                ))}
            </Space>
            ))}
        </Space>
      </Space>
    ));
  };


  return (
    <CardComponent styles={{body:{padding:0,justifySelf:'center'}}} >
    <Space direction='vertical'>
    <Space>
        <Typography.Title level={4} style={{margin:0}}>{data.name}</Typography.Title>
        <Typography.Title level={4} style={{margin:0}}>-</Typography.Title>
        <Typography.Title  level={4} style={{margin:0}}>({data.language})</Typography.Title>
    </Space>
    <Space>
        <Typography>{data.type}</Typography>
    </Space>
    </Space>
    <Divider style={{margin:10}}/>

    <CardComponent width={1200}
    tabList={tabList}
    onTabChange={onTabChange}
    activeTabKey={activeTabKey}
    >
        
       
            {renderSeats()}
      

    </CardComponent>

      </CardComponent>
  )
}

export default Screens



